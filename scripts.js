function disableElements0(){


  var nodes2 = document.getElementById("subject0_1");
  if(window.getComputedStyle(nodes2).display === "inline"){
  nodes2.style.display = 'none';
  var imageClose = document.getElementById("arrowDown0").src = "arrows2.png";

  }
  else{
    nodes2.style.display = 'inline';
    var imageOpen= document.getElementById("arrowDown0").src = "arrows.png";
  }
}


